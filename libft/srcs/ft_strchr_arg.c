#include "libft.h"
#include <stdio.h>
size_t	ft_strchr_arg(const char *str, int c)
{
	int i;

	i = 0;
	while (str[++i] != (char)c)
		if ((str[i] == '\0') || (str[i] == '%'))
			return (0);
	return (i);
}
