#include "libft.h"

char	*ft_strcat_char(char *dest, const char c)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	dest[i] = c;
	dest[i + 1] = '\0';
	return (dest);
}
