#include "libft.h"

int		ft_atoi(const char *str)
{
	int k;
	int i;
	int sign;

	k = 0;
	i = 0;
	sign = 1;
	while ((str[i] != '\0') && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	if ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		k = k * 10 + (str[i] - '0');
		i++;
	}
	return (sign * k);
}
